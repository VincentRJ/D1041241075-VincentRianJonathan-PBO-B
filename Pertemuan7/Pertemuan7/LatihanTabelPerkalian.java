package Pertemuan7;

public class LatihanTabelPerkalian {
  public static void main(String[] args) {
    int angka = 7; // Menginisialisasi angka yang akan dikalikan
    System.out.println("=== TABEL PERKALIAN 7 ===");

    // Perulangan untuk mengalikan nilai 7 dari range 1-12
    for(int perulangan = 1; perulangan <=12; perulangan++){
      System.out.println("7 x " + perulangan + " = " + angka * perulangan); 
    }
  }
}

