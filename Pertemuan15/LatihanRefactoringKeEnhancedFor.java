package Pertemuan15;

public class LatihanRefactoringKeEnhancedFor {
  public static void main(String[] args) {
    // Mendeklarasi array dan menginisialisasi atributnya dengan nama buah
    String[] buah = {"Apel", "Jeruk", "Mangga", "Anggur"};
    // Refactored dengan enhanced for untuk menampilkan isi array 
    for (String b : buah) {
      System.out.println("Buah: " + b);
    }
  }
}

